	<h2 id="description">Description</h2>
	<p>This lists the menu items contained in a menu. This screen allows you to create, view, edit, and delete items for a menu. Items may be an internal link (a link to another page on this site), an external link (a link to some other site), a visual separater between items, or a block of content (typically used for "mega menus").</p>

	<h2 id="columns">Columns</h2>

	<p>In the table containing the users from your site, you will see different columns. Here you can read what they mean and what is displayed in that column.</p>

	<dl>
		<dt>Checkbox</dt><dd>Check this box to select one or more items. To select all items, check the box in the column heading. After one or more boxes are checked, click a toolbar button to take an action on the selected item or items. Many toolbar actions, such as Publish and Unpublish, can work with multiple items. Others, such as Edit, only work on one item at a time. If multiple items are checked and you press Edit, the first item will be opened for editing.</dd>
		<dt>Title</dt><dd>The name of the item. You can open the item for editing by clicking on the Title.</dd>
		<dt>URL</dt><dd>The number of users in this role.</dd>
		<dt>Published</dt><dd>Whether the item has been published or not. You can change the Published state by clicking on the icon in this column.</dd>
		<dt>Access</dt><dd>The viewing level access for this item.</dd>
		<dt>Ordering</dt><dd>Menu items are always listed int he order they're displayed in the menu. Use the dots to drag menu items around to change the ordering.</dd>
	</dl>

	<h2 id="filters">List Filters</h2>

	<dl>
		<dt>Menu</dt>
		<dd>Allows you to choose a different menu item to display the items within.</dd>

		<dt>State</dt>
		<dd>Lets you show only items with the selected published state.

			<ul>
				<li><i>- All states -</i> Show all items regardless of state.</li>
				<li><i>Published:</i> Shows only items that are Published.</li>
				<li><i>Unpublished:</i> Shows only items that are Unpublished.</li>
				<li><i>Trashed:</i> Shows only items that are Trashed. <strong>Important Note:</strong> To permanently delete items:
					<ol>
						<li>Change the status of the items to Trashed.</li>
						<li>Change the Status filter to Trashed. At this point the trashed items will show and an icon called "Empty trash" will show in the toolbar.</li>
						<li>Select the desired trashed items and click on "Empty Trash" in the toolbar. The items will be permanently deleted.</li>
					</ol>
				</li>
			</ul>
		</dd>
		<dt>Access Level</dt><dd>Lets you show only items that have a specified viewing access level. The list box will show the access levels defined for your site.</dd>
	</dl>

	<h2 id="toolbar">Toolbar</h2>

	<p>At the top right you will see the toolbar:</p>

	<dl>
		<dt>Save</dt><dd>Save current settings.</dd>
		<dt>Cancel</dt><dd>Cancel any changes and revert to saved settings.</dd>
		<dt>New</dt><dd>Opens the editing screen to create a new user.</dd>
		<dt>Delete</dt><dd>Deletes the selected users. <strong>Requires selecting one or more records</strong>.</dd>
		<dt>Help</dt><dd>Opens this help screen.</dd>
	</dl>
